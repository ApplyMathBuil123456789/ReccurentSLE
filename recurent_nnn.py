class SpectralReconstructor(nn.Module):
    """
    Signal reconstruction using spectral basis functions

    Trigonometric basis:
        φ_{2k}(t) = √2 * sin(2πkt)
        φ_{2k+1}(t) = √2 * cos(2πkt)
    """

    def __init__(self, max_basis: int, T: float = 1.0):
        super().__init__()
        self.max_basis = max_basis
        self.T = T

        # Precompute time grid
        self.register_buffer('t_grid', None)
        self.register_buffer('basis_cache', None)

    def _get_phi_matrix1(self, j_vals: torch.Tensor, t_vals: torch.Tensor) -> torch.Tensor:
        """
        Creates trigonometric basis matrix

        Args:
            j_vals: Basis indices (1, 2, ..., l)
            t_vals: Time points [0, T]

        Returns:
            phi: (l, M) basis matrix
        """
        # floor division by 2 for sin/cos
        j_half = torch.floor(j_vals / 2)

        # Angles: 2π * (j//2) * t
        angles = 2 * math.pi * torch.tensordot(j_half, t_vals, dims=0)

        # Even indices -> sin, odd indices -> cos
        is_even = ((j_vals % 2) == 0).float().unsqueeze(1)

        sqrt2 = math.sqrt(2.0)
        sin_part = sqrt2 * torch.sin(angles)
        cos_part = sqrt2 * torch.cos(angles)

        phi = is_even * sin_part + (1 - is_even) * cos_part

        # Set first column to 1 (DC component)
        ones_col = torch.ones(phi.size(0), 1, device=phi.device)
        phi = torch.cat([ones_col, phi], dim=1)

        # Remove last column for correct size
        return phi[:, :-1]
    
    def _get_phi_matrix(self, j_vals: torch.Tensor, t_vals: torch.Tensor) -> torch.Tensor:
        """
        j_vals: 1, 2, ..., l  (l basis functions)
        returns: (l, M) orthonormal basis matrix
          j=1 -> phi_1 = 1 (DC)
          j=2 -> phi_2 = sqrt(2)*sin(2π·1·t)
          j=3 -> phi_3 = sqrt(2)*cos(2π·1·t)
          j=4 -> phi_4 = sqrt(2)*sin(2π·2·t)
          ...
        """
        l = j_vals.shape[0]
        M = t_vals.shape[0]
        device = t_vals.device
        sqrt2 = math.sqrt(2.0)

        # DC component (j=1)
        dc = torch.ones(1, M, device=device)   # (1, M)
        if l == 1:
            return dc

        # Remaining basis functions (j=2..l)
        j_rem = j_vals[1:]                     # (l-1,)
        k = torch.floor(j_rem / 2)             # wave number
        angles = 2 * math.pi * torch.tensordot(k, t_vals, dims=0)  # (l-1, M)

        # even j -> sin, odd j -> cos (j starts from 2)
        is_sin = ((j_rem % 2) == 0).float().unsqueeze(1)  # (l-1, 1)

        sin_part = sqrt2 * torch.sin(angles)
        cos_part = sqrt2 * torch.cos(angles)

        phi_rest = is_sin * sin_part + (1 - is_sin) * cos_part  # (l-1, M)
        phi = torch.cat([dc, phi_rest], dim=0)                  # (l, M)
        return phi

    def compute_optimal_l(
        self,
        kappa: torch.Tensor,
        alpha: torch.Tensor,
        r: torch.Tensor,
        epsilon: float,
        max_n: int
    ) -> torch.Tensor:
        """
        Computes the optimal basis length

        Condition: exp(k*l^α) * Σexp(k*m^α) - Σexp(2k*m^α) ≤ r/ε²

        Args:
            kappa: κ parameter
            alpha: α parameter
            r: r parameter
            epsilon: Noise level
            max_n: Maximum number of basis functions

        Returns:
            Optimal l (int)
        """
        device = kappa.device

        # l = 1, 2, ..., max_n
        l_range = torch.arange(1, max_n + 1, dtype=torch.float32, device=device)

        # exp(k * l^α)
        exp_kl_alpha = torch.exp(kappa * (l_range ** alpha))

        # Cumulative sums
        sum1 = torch.cumsum(exp_kl_alpha, dim=0)           # Σ exp(k*m^α)
        sum2 = torch.cumsum(torch.exp(2 * kappa * (l_range ** alpha)), dim=0)

        # Check condition
        condition = exp_kl_alpha * sum1 - sum2
        threshold = r / (epsilon ** 2)

        mask = (condition <= threshold)

        if mask.any():
            # Largest l satisfying the condition
            return (mask.nonzero(as_tuple=False).max()).int() + 1
        else:
            # If no l matches, return minimum value
            return torch.tensor(3, dtype=torch.int32, device=device)

    def forward(
        self,
        Y: torch.Tensor,
        alpha: torch.Tensor,
        kappa: torch.Tensor,
        r: torch.Tensor,
        epsilon: float
    ) -> torch.Tensor:
        """
        Signal reconstruction for a single parameter set

        Args:
            Y: (batch, M) - input signal
            alpha, kappa, r: Scalar parameters
            epsilon: Noise level

        Returns:
            S_hat: (batch, M) - reconstructed signal
        """
        batch_size, M = Y.shape
        device = Y.device

        # Optimal basis length
        l = int(self.compute_optimal_l(kappa, alpha, r, epsilon, M).item())
        l = min(l, M - 1)  # Limit

        # Time grid
        t_vals = torch.linspace(0, self.T, M, device=device)

        # Basis matrices
        j_vals = torch.arange(1, l + 1, dtype=torch.float32, device=device)
        phi_t = self._get_phi_matrix(j_vals, t_vals)  # (l, M)

        # Fourier coefficients (directly from Y)
        # omega = (1/M) * Y * φ^T
        omega = torch.matmul(Y, phi_t.T) / M  # (batch, l)

        # Tikhonov filter: λ_j = 1 - exp(-κ(l^α - j^α))
        l_float = float(l)
        j_float = j_vals  # (l,)

        lambda_j = 1.0 - torch.exp(-kappa * ((l_float ** alpha) - (j_float ** alpha)))

        # Weighted coefficients
        weighted = omega * lambda_j.unsqueeze(0)  # (batch, l)

        # Signal reconstruction
        S_hat = torch.matmul(weighted, phi_t)  # (batch, M)

        return S_hat


