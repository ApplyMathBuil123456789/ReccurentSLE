    @staticmethod
    def _compute_snr(clean: np.ndarray, noisy: np.ndarray) -> float:
        """Compute Signal-to-Noise Ratio (dB)"""
        signal_power = np.mean(clean**2)
        noise_power = np.mean((noisy - clean)**2)
        return 10 * np.log10(signal_power / noise_power)


