def heavisine(t):
    """
    HeaviSine signal - test signal proposed by Donoho (1994)
    f(t) = 4 * sin(4 * pi * t) - sgn(t - 0.3) - sgn(0.72 - t)
    """
    # 4*sin(4*pi*t) part
    sin_part = 4 * np.sin(4 * np.pi * t)

    # sgn(t - 0.3) part: -1 if t < 0.3, 1 if t >= 0.3
    sgn1 = np.where(t < 0.3, -1, 1)

    # sgn(0.72 - t) part: 1 if t < 0.72, -1 if t >= 0.72
    sgn2 = np.where(t < 0.72, 1, -1)

    return sin_part - sgn1 - sgn2


