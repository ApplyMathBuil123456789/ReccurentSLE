import numpy as np

def compute_snr(original, denoised):
    """
    Computes Signal-to-Noise Ratio (SNR).
    Formula: SNR = 10 * log10( sum(x^2) / sum((y - x)^2) )

    Parameters:
    original : ndarray - original noise-free signal (x)
    denoised : ndarray - denoised signal (y)

    Returns:
    snr : float - SNR value in decibels
    """
    original = np.asarray(original)
    denoised = np.asarray(denoised)

    signal_power = np.sum(original ** 2)
    noise_power = np.sum((denoised - original) ** 2)

    # Avoid division by very small numbers
    if noise_power == 0:
        return np.inf

    snr = 10 * np.log10(signal_power / noise_power)
    return snr

def compute_mse(original, denoised):
    """
    Computes Mean Squared Error (MSE).
    Formula: MSE = (1/n) * sum((y - x)^2)

    Parameters:
    original : ndarray - original noise-free signal (x)
    denoised : ndarray - denoised signal (y)

    Returns:
    mse : float - mean squared error
    """
    original = np.asarray(original)
    denoised = np.asarray(denoised)

    mse = np.mean((denoised - original) ** 2)
    return mse

def compute_ncc(original, denoised):
    """
    Computes Normalized Cross-Correlation (NCC).
    Formula: NCC = sum(x * y) / sqrt( sum(x^2) * sum(y^2) )

    Parameters:
    original : ndarray - original noise-free signal (x)
    denoised : ndarray - denoised signal (y)

    Returns:
    ncc : float - correlation coefficient (0 to 1, 1 is best)
    """
    original = np.asarray(original)
    denoised = np.asarray(denoised)

    numerator = np.sum(original * denoised)
    denominator = np.sqrt(np.sum(original ** 2) * np.sum(denoised ** 2))

    if denominator == 0:
        return 0.0

    ncc = numerator / denominator
    return ncc

